export function isValidWord(word: string): boolean {
  return DICTIONARY_WORDS.has(word.toUpperCase());
}

export function validateWord(word: string): { isValid: boolean; message?: string } {
  const upperWord = word.toUpperCase();
  
  if (!word.trim()) {
    return { isValid: false, message: 'Please enter a word' };
  }
  
  if (!/^[A-Za-z]+$/.test(word)) {
    return { isValid: false, message: 'Word must contain only letters' };
  }
  
  if (!DICTIONARY_WORDS.has(upperWord)) {
    return { isValid: false, message: 'Not a valid English word' };
  }
  
  return { isValid: true };
}